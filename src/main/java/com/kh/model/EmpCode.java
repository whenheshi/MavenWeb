package com.kh.model;


/**
 * 钉钉工号信息
 * @author Administrator
 *
 */
public class EmpCode {
	//工号
	private String code;
	//姓名
	private String name;
	//钉钉ID
	private String id;
	
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
}
